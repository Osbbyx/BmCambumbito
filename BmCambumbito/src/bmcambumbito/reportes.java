    public int MayorValor(int valor_rojo, int valor_verde, int valor_azul) {
        if (valor_rojo > valor_verde && valor_rojo > valor_azul) {
            return valor_rojo;
        } else if (valor_verde > valor_azul) {
            return valor_verde;
        } else {
            return valor_azul;
        }
    }

